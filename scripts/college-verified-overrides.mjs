/** Replace generic LMS mentions without mangling compound names (e.g. CarmenCanvas). */
export function applyLms(text, lmsName) {
  if (!text || !lmsName) return text;
  let out = text.replace(/CarmenCarmenCanvas/g, "CarmenCanvas");
  if (out.includes(lmsName)) return out;

  const chain = [
    /ONE\.UF\/Canvas/g,
    /ELMS\/Canvas/g,
    /D2L Brightspace/g,
    /CarmenCanvas/g,
    /Brightspace/g,
    /Blackboard/g,
    /Moodle/g,
    /Sakai/g,
    /\bONE\.UF\b/g,
    /\bELMS\b/g,
    /\bD2L\b/g,
    /\bCanvas\b/g,
  ];
  for (const re of chain) {
    if (re.test(out) && lmsName !== out.match(re)?.[0]) {
      out = out.replace(re, lmsName);
    }
  }
  return out;
}

export function mergeCollegeWithVerified(college) {
  const v = VERIFIED_OVERRIDES[college.slug];
  if (!v) return college;
  const lms = v.lmsName;
  const merged = {
    ...college,
    ...v,
    departments: v.departments ?? college.departments,
    professorCards: v.professorCards ?? college.professorCards,
    testimonials: v.testimonials ?? college.testimonials,
    faqs: v.faqs ?? college.faqs,
  };
  if (lms) {
    merged.alertBody = applyLms(merged.alertBody, lms);
    merged.calendarSubtitle = applyLms(merged.calendarSubtitle, lms);
    merged.faqSubtitle = applyLms(merged.faqSubtitle, lms);
    if (merged.faqs) {
      merged.faqs = merged.faqs.map((f) => ({
        q: applyLms(f.q, lms),
        a: applyLms(f.a, lms),
      }));
    }
    if (merged.testimonials) {
      merged.testimonials = merged.testimonials.map((t) => ({
        ...t,
        quote: applyLms(t.quote, lms),
      }));
    }
  }
  delete merged.lmsName;
  return merged;
}
